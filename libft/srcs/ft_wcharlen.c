#include "../includes/libft.h"

int	ft_wcharlen(wchar_t s)
{
	int j;

	j = 0;
	if ((int)s < 0x80)
		j += 1;
	else if ((int)s < 0x800)
		j += 2;
	else if ((int)s < 0x10000)
		j += 3;
	else
		j += 4;
	return (j);
}
