#include "../includes/ft_printf.h"

void	ft_add_str_n(t_env *arg, const char *s, int n)
{
	int i;

	i = 0;
	while (i < n && s[i] != '\0')
	{
		ft_add_c_to_buff(arg, s[i]);
		i++;
	}
}
